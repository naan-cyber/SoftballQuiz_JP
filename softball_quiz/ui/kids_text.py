NORMALIZATIONS: tuple[tuple[str, str], ...] = (
    ("一つ", "ひとつ"),
    ("一アウト", "1アウト"),
    ("二アウト", "2アウト"),
)


def kids_text(text: str) -> str:
    readable = text
    for source, target in sorted(GRADE_3_AND_UP_READINGS, key=lambda item: len(item[0]), reverse=True):
        readable = readable.replace(source, target)
    for source, target in NORMALIZATIONS:
        readable = readable.replace(source, target)
    return _dedupe_readings(readable)


def _dedupe_readings(text: str) -> str:
    cleanups = (
        ("フォースアウト（ふむだけのアウト）（ふむだけのアウト）", "フォースアウト（ふむだけのアウト）"),
        ("タッチアップ（とったあとに走ること）（とったあとに走ること）", "タッチアップ（とったあとに走ること）"),
        ("バッターボックス（打つ（うつ）場所（ばしょ））（打つ（うつ）場所（ばしょ））", "バッターボックス（打つ（うつ）場所（ばしょ））"),
        ("満るい（まんるい）（まんるい）", "満るい（まんるい）"),
        ("場所（ばしょ）（ばしょ）", "場所（ばしょ）"),
        ("自分（じぶん）（じぶん）", "自分（じぶん）"),
        ("持っ（もっ）て（もって）", "持って（もって）"),
        ("戻っ（もどっ）て（もどって）", "戻って（もどって）"),
        ("動い（うごい）て（うごいて）", "動いて（うごいて）"),
        ("始まり（はじまり）ます（はじまります）", "始まります（はじまります）"),
    )
    readable = text
    while True:
        previous = readable
        for source, target in cleanups:
            readable = readable.replace(source, target)
        readable = re.sub(r"（([^（）]+)）(?:（\1）)+", r"（\1）", readable)
        if readable == previous:
            return readable
